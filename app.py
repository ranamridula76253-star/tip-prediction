import streamlit as st
import pickle
import pandas as pd
model=pickle.load(open("tips_model.pkl","rb"))
st.title("Tip Predicton App")
total_bill=st.number_input("Total Bill")
size=st.number_input("Size")
sex=st.selectbox("Sex",["Male","Female"])
smoker=st.selectbox("smoker",["Yes","No"])
day=st.selectbox("Day",["Thur","Friday","Saturday"])
time=st.selectbox("Time",["Lunch","dinner"])
input_data=pd.DataFrame({
    'total_bill':[total_bill],
    'size':[size],
    'sex':[sex],
    'smoker':[smoker],
    'day':[day],
    'time':[time]
})
if st.button("Predict Tip"):
    prediction=model.predict(input_data)
    st.sucess(f"Predicted Tip:{prediction[0]:2f}")